# Arithmetic operators
num1 = 56
num2 = 2
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus returns the remainder after division

# Comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) # Equal to
print(num1 != num2) # Not equal to

# Assignment operators
x = 14
print(x)

x += 2
print(x)

# Logic operator - and,or,not
number = 5
print( number < 10 and number > 20)
print( number < 10 or number > 20)
print( not(number < 10 or number > 20))

# Operator precedence
result = (5 + 10 * 2 / 1 - 4)
print(result)

